
#ifndef HICHAN_APP_GAME_H
#define HICHAN_APP_GAME_H

/////////////////////////////////////////////////////////////////////////////////
///	@		前方宣言
class Window;
typedef std::weak_ptr< Window > WindowWeakPtr;

///	=============================================================================
///	@brief		Gameクラス
///	=============================================================================
class Game
{
public:
	///	-----------------------------------------------------------------------------
	///	@brief		コンストラクタ
	///	@param		window  既存ウィンドウのweak_ptr
	///	-----------------------------------------------------------------------------
    Game( const WindowWeakPtr& window );

public:
	///	-----------------------------------------------------------------------------
	///	@brief		ゲームの初期化処理を行う
	///	@param		初期化成功時trueを、失敗時falseを返す
	///	-----------------------------------------------------------------------------
    bool Initialize();

	///	-----------------------------------------------------------------------------
	///	@brief		ゲームの解放処理を行う
	///	-----------------------------------------------------------------------------
    void Finalize();

	///	-----------------------------------------------------------------------------
	///	@brief		ゲーム１フレームの処理を行う
	///	-----------------------------------------------------------------------------
    void Run();


private:
	///	-----------------------------------------------------------------------------
	///	@			メンバ
	///	-----------------------------------------------------------------------------
    WindowWeakPtr window;   ///<    既存ウィンドウを指すポインタ
};

#endif