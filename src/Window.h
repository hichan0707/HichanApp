#ifndef HICHAN_APP_WINDOW_H
#define HICHAN_APP_WINDOW_H


///	=============================================================================
///	@brief		HWNDのラッパークラス
///	=============================================================================
class Window
{
public:
	///	-----------------------------------------------------------------------------
	///	@brief		コンストラクタ
	///	-----------------------------------------------------------------------------
	Window( HWND hwnd );

public:
	///	-----------------------------------------------------------------------------
	///	@brief		ウィンドウが閉じられたか
	///	-----------------------------------------------------------------------------
	bool IsClosed()const{ return ::IsWindow( hwnd ) == FALSE; }

private:
	///	-----------------------------------------------------------------------------
	///	@			変数
	///	-----------------------------------------------------------------------------
	HWND hwnd;
};


#endif